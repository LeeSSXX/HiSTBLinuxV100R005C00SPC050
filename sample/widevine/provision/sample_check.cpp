#define kAES128BlockSize 16
int AES_CTR_decrypt(const uint8_t *key, uint8_t *iv, uint8_t *out,
                                   const uint8_t *in, size_t length)
{
     AES_KEY aesKey;

    if (AES_set_decrypt_key(key, 128, &aesKey) != 0) {
        printf("no AES KEY\n");
        return -1;
    }

    size_t k, r = length % kAES128BlockSize;

    if (r) {
        k = length - r - kAES128BlockSize;
    } else {
        k = length;
    }

    AES_cbc_encrypt(in, out, k, &aesKey, iv, 0);

    if (r) {
        // cipher text stealing - Schneier Figure 9.5 p 196
        unsigned char peniv[kAES128BlockSize] = {0};
        memcpy(peniv, in + k + kAES128BlockSize, r);

        AES_cbc_encrypt(in + k, out + k, kAES128BlockSize, &aesKey, peniv, 0);

        // exchange the final plaintext and ciphertext
        for (size_t i = 0; i < r; i++) {
            *(out + k + kAES128BlockSize + i) = *(out + k + i);
            *(out + k + i) = *(in + k + kAES128BlockSize + i);
        }
        AES_cbc_encrypt(out + k, out + k, kAES128BlockSize, &aesKey, iv, 0);
    }
    return 0;
}

